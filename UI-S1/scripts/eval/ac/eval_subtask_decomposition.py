"""Eval: Sub-Task Decomposition Agent Evaluation.

Tests whether decomposing complex multi-phase tasks into simpler sub-task
instructions improves agent performance.

Design:
  - Phase segmentation: consecutive same-action-type steps = 1 phase
  - Stage 1: Use VLM to decompose full goal into phase-level sub-task goals
  - Stage 2: Evaluate each step with sub-task goal (vs. full goal baseline)
  - Comparison with C4+C7 baseline (full goal, already collected)

Conditions:
  A) full_goal: Original full task goal (baseline from C4+C7 greedy)
  B) subtask_goal: Phase-level sub-task goal generated by VLM
  C) subtask_goal_reset: Same as B but reset history at phase boundaries

Each step is evaluated independently with GT screenshots (no AR stop-on-error).
"""

import argparse
import copy
import json
import os
import re
import sys
import traceback
import time
from collections import defaultdict
from concurrent.futures import ThreadPoolExecutor, as_completed
from threading import Lock

from ac_utils import (
    load_ac_trajectories, fix_line, init_format, save_jsonl, save_json,
    evaluate_android_control_action, find_last_image_ele, slim_messages,
    call_mobile_agent_vllm, safe_parse_response, _json_default,
)
from evaluation.qwenvl_utils import END_POINT, image_to_data_url, message_translate
from openai import OpenAI
from PIL import Image

result_lock = Lock()
fm = None


# ─────────────────────────────────────────────
# Phase segmentation
# ─────────────────────────────────────────────

def segment_phases(episode):
    """Segment trajectory into phases (consecutive same action type).

    Returns list of phases: [{'action_type': str, 'step_ids': [int], 'steps': [dict]}]
    """
    phases = []
    current_type = None
    current_phase = None

    for i, step in enumerate(episode['steps']):
        action_type = step['action_content']['action']
        if action_type != current_type:
            if current_phase is not None:
                phases.append(current_phase)
            current_type = action_type
            current_phase = {
                'action_type': action_type,
                'step_ids': [i],
                'steps': [step],
            }
        else:
            current_phase['step_ids'].append(i)
            current_phase['steps'].append(step)

    if current_phase is not None:
        phases.append(current_phase)

    return phases


# ─────────────────────────────────────────────
# Sub-task goal generation via VLM
# ─────────────────────────────────────────────

DECOMPOSE_PROMPT = """You are a task decomposition agent. Given a user's high-level goal for a mobile phone task, decompose it into {n_phases} sequential sub-tasks.

Full Goal: {goal}

The task is completed in {n_phases} sequential phases:
{phase_descriptions}

For each phase, write a specific, actionable sub-task instruction that tells an agent exactly what to do. The instruction should be self-contained and understandable without the full goal context.

Output ONLY a JSON array (no extra text):
[
  {{"phase": 0, "subtask_goal": "..."}},
  {{"phase": 1, "subtask_goal": "..."}},
  ...
]"""


def generate_subtask_goals(episode, phases, model_name):
    """Use the VLM to decompose the full goal into phase-level sub-task goals.

    Args:
        episode: Full episode dict with 'goal'.
        phases: List of phase dicts from segment_phases().
        model_name: Model name for vLLM.

    Returns:
        List of sub-task goal strings, one per phase.
    """
    # Build phase descriptions
    phase_descs = []
    for i, phase in enumerate(phases):
        n_steps = len(phase['step_ids'])
        action_type = phase['action_type']
        phase_descs.append(f"  Phase {i}: {action_type} ({n_steps} step{'s' if n_steps > 1 else ''})")

    prompt = DECOMPOSE_PROMPT.format(
        goal=episode['goal'],
        n_phases=len(phases),
        phase_descriptions="\n".join(phase_descs),
    )

    messages = [{"role": "user", "content": [{"type": "text", "text": prompt}]}]

    # Call model (text-only, no image needed for decomposition)
    for attempt in range(3):
        try:
            bot = OpenAI(api_key="EMPTY", base_url=END_POINT, timeout=120)
            resp = bot.chat.completions.create(
                model=model_name,
                messages=messages,
                extra_body={"top_k": 1},
                max_tokens=1024,
            )
            response_text = resp.choices[0].message.content

            # Parse JSON from response
            subtask_goals = _parse_decomposition(response_text, len(phases))
            if subtask_goals is not None:
                return subtask_goals
        except Exception as e:
            if attempt < 2:
                time.sleep(2)
            else:
                print(f"Decomposition failed for goal '{episode['goal'][:60]}...': {e}")

    # Fallback: use full goal for all phases
    return [episode['goal']] * len(phases)


def _parse_decomposition(response_text, n_phases):
    """Parse the VLM's decomposition response into a list of sub-task goals."""
    # Try direct JSON parse
    try:
        parsed = json.loads(response_text)
        if isinstance(parsed, list) and len(parsed) == n_phases:
            return [item.get('subtask_goal', item.get('goal', '')) for item in parsed]
    except (json.JSONDecodeError, AttributeError):
        pass

    # Try extracting JSON array from response
    match = re.search(r'\[[\s\S]*\]', response_text)
    if match:
        try:
            parsed = json.loads(match.group(0))
            if isinstance(parsed, list) and len(parsed) >= n_phases:
                return [item.get('subtask_goal', item.get('goal', '')) for item in parsed[:n_phases]]
        except (json.JSONDecodeError, AttributeError):
            pass

    return None


# ─────────────────────────────────────────────
# Evaluation: run agent with sub-task goals
# ─────────────────────────────────────────────

def evaluate_step_with_goal(fixed_episode, step_id, goal, state, model_name, n_history_image_limit):
    """Evaluate a single step using a specific goal instruction.

    Args:
        fixed_episode: Episode with check_options.
        step_id: Current step index.
        goal: Goal string to use (full or sub-task).
        state: Current conversation state (from gen_next_round).
        model_name: Model name for vLLM.
        n_history_image_limit: Max history images.

    Returns:
        Dict with prediction results and updated state.
    """
    global fm

    current_check = fixed_episode['steps'][step_id]['check_options']
    gt_action = fixed_episode['steps'][step_id]['action_content']

    # Create modified episode with the target goal
    ep_modified = copy.deepcopy(fixed_episode)
    ep_modified['goal'] = goal

    # gen_next_round builds messages with ep_modified['goal']
    new_state = fm.gen_next_round(ep_modified, state, previous_model_response=None)
    if new_state is None:
        return None, state

    messages = slim_messages(
        messages=new_state['messages'],
        num_image_limit=n_history_image_limit,
    )
    _, width, height, resized_width, resized_height = find_last_image_ele(messages)

    # Greedy prediction
    try:
        response = call_mobile_agent_vllm(messages=messages, model_name=model_name)
        pred = safe_parse_response(fm, response)
        pred_action = pred['action_content']
        type_match, extract_match = evaluate_android_control_action(
            pred_action, current_check,
            width, height, resized_width, resized_height,
        )
    except Exception as e:
        pred_action = None
        type_match = False
        extract_match = False

    result = {
        'step_num': step_id,
        'type_match': type_match,
        'extract_match': extract_match,
        'pred_action': pred_action,
        'gt_action': gt_action,
        'gt_action_type': gt_action['action'],
        'goal_used': goal[:200],
    }

    return result, new_state


def process_episode(episode, args):
    """Process a single episode with sub-task decomposition evaluation.

    Runs three conditions:
    B) subtask_goal: Sub-task goal with continuous history
    C) subtask_goal_reset: Sub-task goal with history reset at phase boundaries
    """
    global fm

    fixed = fix_line(copy.deepcopy(episode))
    num_steps = len(fixed['steps'])
    goal = episode['goal']

    # Phase segmentation
    phases = segment_phases(fixed)
    n_phases = len(phases)

    # Build step->phase mapping
    step_to_phase = {}
    for pi, phase in enumerate(phases):
        for sid in phase['step_ids']:
            step_to_phase[sid] = pi

    # Stage 1: Generate sub-task goals
    subtask_goals = generate_subtask_goals(episode, phases, args.model_name)

    # Stage 2: Evaluate each step under different conditions

    # Condition B: subtask_goal (continuous history, but with sub-task goal)
    results_subtask = []
    state_b = None
    for step_id in range(num_steps):
        phase_idx = step_to_phase[step_id]
        sub_goal = subtask_goals[phase_idx]

        result, state_b = evaluate_step_with_goal(
            fixed, step_id, sub_goal, state_b,
            args.model_name, args.n_history_image_limit,
        )
        if result is None:
            break
        result['condition'] = 'subtask_goal'
        result['phase_idx'] = phase_idx
        results_subtask.append(result)

    # Condition C: subtask_goal_reset (reset history at phase boundaries)
    results_subtask_reset = []
    state_c = None
    prev_phase = -1
    for step_id in range(num_steps):
        phase_idx = step_to_phase[step_id]
        sub_goal = subtask_goals[phase_idx]

        # Reset state at phase boundaries
        if phase_idx != prev_phase:
            state_c = None
            prev_phase = phase_idx

        result, state_c = evaluate_step_with_goal(
            fixed, step_id, sub_goal, state_c,
            args.model_name, args.n_history_image_limit,
        )
        if result is None:
            break
        result['condition'] = 'subtask_goal_reset'
        result['phase_idx'] = phase_idx
        results_subtask_reset.append(result)

    # Condition A: full_goal baseline (continuous history, full goal)
    results_full = []
    state_a = None
    for step_id in range(num_steps):
        phase_idx = step_to_phase[step_id]

        result, state_a = evaluate_step_with_goal(
            fixed, step_id, goal, state_a,
            args.model_name, args.n_history_image_limit,
        )
        if result is None:
            break
        result['condition'] = 'full_goal'
        result['phase_idx'] = phase_idx
        results_full.append(result)

    # Assemble output
    output = {
        'episode_id': episode.get('episode_id'),
        'goal': goal,
        'num_steps': num_steps,
        'n_phases': n_phases,
        'phases': [
            {
                'phase_idx': i,
                'action_type': p['action_type'],
                'step_ids': p['step_ids'],
                'subtask_goal': subtask_goals[i],
            }
            for i, p in enumerate(phases)
        ],
        'results_full_goal': results_full,
        'results_subtask_goal': results_subtask,
        'results_subtask_goal_reset': results_subtask_reset,
    }

    with result_lock:
        out_path = os.path.join(args.output_dir, 'subtask_eval_results.jsonl')
        with open(out_path, 'a') as f:
            f.write(json.dumps(output, ensure_ascii=False, default=_json_default) + '\n')

    return output


# ─────────────────────────────────────────────
# Analysis: compare conditions
# ─────────────────────────────────────────────

def analyze_results(results, output_dir):
    """Analyze and compare the three conditions."""
    conditions = ['full_goal', 'subtask_goal', 'subtask_goal_reset']
    cond_key_map = {
        'full_goal': 'results_full_goal',
        'subtask_goal': 'results_subtask_goal',
        'subtask_goal_reset': 'results_subtask_goal_reset',
    }

    print("\n" + "=" * 80)
    print("RESULTS: Sub-Task Decomposition Agent Evaluation")
    print("=" * 80)

    # 1. Overall per-step accuracy
    print("\n--- Overall Per-Step Accuracy ---")
    for cond in conditions:
        key = cond_key_map[cond]
        type_correct = sum(s['type_match'] for r in results for s in r[key])
        extract_correct = sum(s['extract_match'] for r in results for s in r[key])
        total = sum(len(r[key]) for r in results)
        print(f"  {cond:25s}: type_match={type_correct}/{total} ({type_correct / total * 100:.1f}%)  "
              f"extract_match={extract_correct}/{total} ({extract_correct / total * 100:.1f}%)")

    # 2. Per-action-type accuracy
    print("\n--- Per-Action-Type Accuracy ---")
    for cond in conditions:
        key = cond_key_map[cond]
        by_type = defaultdict(lambda: {'correct': 0, 'total': 0})
        for r in results:
            for s in r[key]:
                at = s['gt_action_type']
                by_type[at]['total'] += 1
                if s['extract_match']:
                    by_type[at]['correct'] += 1
        print(f"\n  Condition: {cond}")
        print(f"  {'Type':>15s} | {'Correct':>8s} | {'Total':>8s} | {'Accuracy':>8s}")
        for at in sorted(by_type.keys()):
            d = by_type[at]
            acc = d['correct'] / d['total'] * 100 if d['total'] > 0 else 0
            print(f"  {at:>15s} | {d['correct']:>8d} | {d['total']:>8d} | {acc:>7.1f}%")

    # 3. Per-phase-position accuracy
    print("\n--- Per-Phase-Position Accuracy ---")
    for cond in conditions:
        key = cond_key_map[cond]
        by_pos = defaultdict(lambda: {'correct': 0, 'total': 0})
        for r in results:
            for s in r[key]:
                pi = s['phase_idx']
                by_pos[pi]['total'] += 1
                if s['extract_match']:
                    by_pos[pi]['correct'] += 1
        print(f"\n  Condition: {cond}")
        for pi in sorted(by_pos.keys()):
            d = by_pos[pi]
            acc = d['correct'] / d['total'] * 100 if d['total'] > 0 else 0
            print(f"    Phase {pi}: {d['correct']}/{d['total']} = {acc:.1f}%")

    # 4. Simulated TSR comparison
    print("\n--- Simulated TSR (product of per-step accuracy over trajectory) ---")
    for cond in conditions:
        key = cond_key_map[cond]
        success_count = 0
        for r in results:
            if all(s['extract_match'] for s in r[key]) and len(r[key]) == r['num_steps']:
                success_count += 1
        tsr = success_count / len(results) * 100 if results else 0
        print(f"  {cond:25s}: TSR = {success_count}/{len(results)} ({tsr:.1f}%)")

    # 5. Paired comparison: full vs subtask per episode
    print("\n--- Paired Comparison: full_goal vs subtask_goal ---")
    full_better = 0
    subtask_better = 0
    tied = 0
    for r in results:
        full_acc = sum(s['extract_match'] for s in r['results_full_goal'])
        subtask_acc = sum(s['extract_match'] for s in r['results_subtask_goal'])
        if full_acc > subtask_acc:
            full_better += 1
        elif subtask_acc > full_acc:
            subtask_better += 1
        else:
            tied += 1
    print(f"  full_goal better:    {full_better} episodes")
    print(f"  subtask_goal better: {subtask_better} episodes")
    print(f"  tied:                {tied} episodes")

    # 6. By number of phases
    print("\n--- By Number of Phases ---")
    by_nphases = defaultdict(lambda: {c: {'correct': 0, 'total': 0} for c in conditions})
    for r in results:
        np_ = r['n_phases']
        for cond in conditions:
            key = cond_key_map[cond]
            total = len(r[key])
            correct = sum(s['extract_match'] for s in r[key])
            by_nphases[np_][cond]['total'] += total
            by_nphases[np_][cond]['correct'] += correct

    print(f"  {'#Phases':>8s}", end="")
    for cond in conditions:
        print(f" | {cond:>25s}", end="")
    print()
    for np_ in sorted(by_nphases.keys()):
        if np_ > 8:
            continue
        print(f"  {np_:>8d}", end="")
        for cond in conditions:
            d = by_nphases[np_][cond]
            acc = d['correct'] / d['total'] * 100 if d['total'] > 0 else 0
            print(f" | {acc:>24.1f}%", end="")
        print()

    # 7. Phase boundary analysis
    print("\n--- Phase Boundary vs Within-Phase Accuracy ---")
    for cond in conditions:
        key = cond_key_map[cond]
        boundary_correct = 0
        boundary_total = 0
        within_correct = 0
        within_total = 0
        for r in results:
            phases = r['phases']
            for s in r[key]:
                sid = s['step_num']
                pi = s['phase_idx']
                # Is this step the first step of its phase?
                is_boundary = (sid == phases[pi]['step_ids'][0]) and pi > 0
                if is_boundary:
                    boundary_total += 1
                    if s['extract_match']:
                        boundary_correct += 1
                else:
                    within_total += 1
                    if s['extract_match']:
                        within_correct += 1

        boundary_acc = boundary_correct / boundary_total * 100 if boundary_total > 0 else 0
        within_acc = within_correct / within_total * 100 if within_total > 0 else 0
        print(f"  {cond:25s}: boundary={boundary_acc:.1f}% ({boundary_correct}/{boundary_total})  "
              f"within={within_acc:.1f}% ({within_correct}/{within_total})")

    # Save summary
    summary = {
        'total_episodes': len(results),
        'conditions': {},
    }
    for cond in conditions:
        key = cond_key_map[cond]
        type_correct = sum(s['type_match'] for r in results for s in r[key])
        extract_correct = sum(s['extract_match'] for r in results for s in r[key])
        total = sum(len(r[key]) for r in results)
        success_count = sum(
            1 for r in results
            if all(s['extract_match'] for s in r[key]) and len(r[key]) == r['num_steps']
        )
        summary['conditions'][cond] = {
            'type_match': type_correct / total if total > 0 else 0,
            'extract_match': extract_correct / total if total > 0 else 0,
            'total_steps': total,
            'tsr': success_count / len(results) if results else 0,
            'tsr_count': success_count,
        }
    summary['paired'] = {
        'full_better': full_better,
        'subtask_better': subtask_better,
        'tied': tied,
    }
    save_json(summary, os.path.join(output_dir, 'subtask_eval_summary.json'))
    print(f"\nSummary saved to {output_dir}/subtask_eval_summary.json")


def main(args):
    global fm
    fm = init_format()
    os.makedirs(args.output_dir, exist_ok=True)

    out_path = os.path.join(args.output_dir, 'subtask_eval_results.jsonl')
    if os.path.exists(out_path):
        os.remove(out_path)

    data = load_ac_trajectories(jsonl_path=args.jsonl_file, max_episodes=args.max_episodes)

    # Filter to multi-phase episodes (1-phase episodes are trivially the same)
    if args.multi_phase_only:
        filtered = []
        for ep in data:
            phases = segment_phases(fix_line(copy.deepcopy(ep)))
            if len(phases) >= 2:
                filtered.append(ep)
        print(f"Loaded {len(data)} episodes, filtered to {len(filtered)} multi-phase episodes.")
        data = filtered
    else:
        print(f"Loaded {len(data)} episodes.")

    print(f"Running sub-task decomposition evaluation (3 conditions per episode)...")
    print(f"  Model: {args.model_name}")
    print(f"  Output: {args.output_dir}")
    print(f"  Workers: {args.max_workers}")

    results = []
    with ThreadPoolExecutor(max_workers=args.max_workers) as executor:
        futures = {executor.submit(process_episode, ep, args): ep for ep in data}
        for future in as_completed(futures):
            try:
                result = future.result()
                results.append(result)
                if len(results) % 20 == 0:
                    # Quick progress stats
                    full_acc = sum(
                        s['extract_match']
                        for r in results for s in r['results_full_goal']
                    ) / max(1, sum(len(r['results_full_goal']) for r in results))
                    sub_acc = sum(
                        s['extract_match']
                        for r in results for s in r['results_subtask_goal']
                    ) / max(1, sum(len(r['results_subtask_goal']) for r in results))
                    print(f"Progress: {len(results)}/{len(data)} | "
                          f"full_goal: {full_acc:.3f} | subtask_goal: {sub_acc:.3f}")
            except Exception as e:
                print(f"Exception: {e}")
                traceback.print_exc()

    # Analyze results
    analyze_results(results, args.output_dir)

    print(f"\nDone. {len(results)} episodes processed.")


